void cari() {
    DefaultTableModel tbl = new DefaultTableModel();
    tbl.addColumn("npm");
    tbl.addColumn("nama");
    tbl.addColumn("jenis kelamin");
    tbl.addColumn("jurusan");
    
    try {
        String sql = "SELECT * FROM data_mahasiswa WHERE npm LIKE '%" + bCari2.getText() + "%'";
        Connection con = (Connection) Koneksi.getConnection();
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery(sql);
        boolean found = false; // Menandai apakah ada hasil pencarian yang ditemukan
        while (rs.next()) {
            tbl.addRow(new Object[]{
                rs.getString("npm"),
                rs.getString("nama"),
                rs.getString("jenis_kelamin"),
                rs.getString("jurusan")
            });
            found = true; // Set found menjadi true karena ada hasil pencarian
        }
        if (!found) {
            // Jika tidak ada hasil pencarian, tampilkan pesan ke pengguna
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
        tblMhs.setModel(tbl); // Set model tabel di luar loop while
    } catch (Exception e) {
        e.printStackTrace(); // Menampilkan trace stack lengkap
    }
}




    public void table(){
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("npm");
        tbl.addColumn("nama");
        tbl.addColumn("jenis kelamin");
        tbl.addColumn("jurusan");
        
        try {
            Statement st = (Statement) Koneksi.getConnection().createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM data_mahasiswa");
            while(rs.next()){
                tbl.addRow(new Object[]{
                    rs.getString("npm"),
                    rs.getString("nama"),
                    rs.getString("jenis_kelamin"),
                    rs.getString("jurusan")
                });
                tblMhs.setModel(tbl);
            }
            JOptionPane.showMessageDialog(null, "koneksi berhasil");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "koneksi gagal");
        }

    }
