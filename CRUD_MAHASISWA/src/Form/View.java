    private void bEditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bEditMouseClicked
        // TODO add your handling code here:
        String jk = null;
        if (rLaki.isSelected()) {
            jk = "laki - laki";
        } else if (rPerempuan.isSelected()) {
            jk = "perempuan";
        }

        try {
            String sql = "UPDATE data_mahasiswa SET npm=?, nama=?, jenis_kelamin=?, jurusan=? WHERE npm=?";
            Connection con = (Connection) Koneksi.getConnection();
            PreparedStatement pst = con.prepareStatement(sql);
            // Mengatur parameter pada PreparedStatement
            pst.setString(1, tfNpm.getText());
            pst.setString(2, tfName.getText());
            pst.setString(3, jk);
            pst.setString(4, tfJurusan.getText());
            pst.setString(5, tfNpm.getText()); // Kondisi WHERE
            // Eksekusi pernyataan SQL
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "data berhasil di edit");
        } catch (Exception e) {
            // Tanggapan terhadap eksepsi
            JOptionPane.showMessageDialog(null, "Gagal mengedit data: " + e.getMessage());
            e.printStackTrace(); // Menampilkan jejak stack trace
        }
    }//GEN-LAST:event_bEditMouseClicked

